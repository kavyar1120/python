temp=int(input("enter the temperature"))
if temp>=35:
    print("it is extremely hot! stay hydrated and avoid direct sunlight.")
elif temp>=25:
    print("it is warm day! perfect for light clothing.")
elif temp>=15:
    print("it is comfortable day! a light jacket might be needed.")
elif temp>=5:
    print("it is cold!  wear a jacket.")
else:
    print("it is very cold! must wear a heavy jacket and stay warm")
